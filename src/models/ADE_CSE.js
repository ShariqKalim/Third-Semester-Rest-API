const mongoose = require("mongoose");

const ADECSESchema = new mongoose.Schema({
  topic: {
    type: String,
    require: true,
    trim: true,
  },
  date: {
    type: String,
    require: true,
    trim: true,
  },
  time: {
    type: String,
    require: true,
    trim: true,
  },
  link: {
    type: String,
    require: true,
    trim: true,
  },
});

const ADECSE = new mongoose.model("ADE_CSE", ADECSESchema);

module.exports = ADECSE;
