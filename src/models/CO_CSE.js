const mongoose = require("mongoose");

const COCSESchema = new mongoose.Schema({
  topic: {
    type: String,
    require: true,
    trim: true,
  },
  date: {
    type: String,
    require: true,
    trim: true,
  },
  time: {
    type: String,
    require: true,
    trim: true,
  },
  link: {
    type: String,
    require: true,
    trim: true,
  },
});

const COCSE = new mongoose.model("CO_CSE", COCSESchema);

module.exports = COCSE;
