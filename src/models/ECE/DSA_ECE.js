const mongoose = require("mongoose");

const DSAECESchema = new mongoose.Schema({
  topic: {
    type: String,
    require: true,
    trim: true,
  },
  date: {
    type: String,
    require: true,
    trim: true,
  },
  time: {
    type: String,
    require: true,
    trim: true,
  },
  link: {
    type: String,
    require: true,
    trim: true,
  },
});

const DSAECE = new mongoose.model("DSA_ECE", DSAECESchema);

module.exports = DSAECE;
