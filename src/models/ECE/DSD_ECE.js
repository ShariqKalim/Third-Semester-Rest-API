const mongoose = require("mongoose");

const DSDECESchema = new mongoose.Schema({
  topic: {
    type: String,
    require: true,
    trim: true,
  },
  date: {
    type: String,
    require: true,
    trim: true,
  },
  time: {
    type: String,
    require: true,
    trim: true,
  },
  link: {
    type: String,
    require: true,
    trim: true,
  },
});

const DSDECE = new mongoose.model("DSD_ECE", DSDECESchema);

module.exports = DSDECE;
