const mongoose = require("mongoose");

const EDECESchema = new mongoose.Schema({
  topic: {
    type: String,
    require: true,
    trim: true,
  },
  date: {
    type: String,
    require: true,
    trim: true,
  },
  time: {
    type: String,
    require: true,
    trim: true,
  },
  link: {
    type: String,
    require: true,
    trim: true,
  },
});

const EDECE = new mongoose.model("ED_ECE", EDECESchema);

module.exports = EDECE;
