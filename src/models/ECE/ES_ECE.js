const mongoose = require("mongoose");

const ESECESchema = new mongoose.Schema({
  topic: {
    type: String,
    require: true,
    trim: true,
  },
  date: {
    type: String,
    require: true,
    trim: true,
  },
  time: {
    type: String,
    require: true,
    trim: true,
  },
  link: {
    type: String,
    require: true,
    trim: true,
  },
});

const ESECE = new mongoose.model("ES_ECE", ESECESchema);

module.exports = ESECE;
