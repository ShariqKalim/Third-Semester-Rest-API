const mongoose = require("mongoose");

const MATHECESchema = new mongoose.Schema({
  topic: {
    type: String,
    require: true,
    trim: true,
  },
  date: {
    type: String,
    require: true,
    trim: true,
  },
  time: {
    type: String,
    require: true,
    trim: true,
  },
  link: {
    type: String,
    require: true,
    trim: true,
  },
});

const MATHECE = new mongoose.model("MATH_ECE", MATHECESchema);

module.exports = MATHECE;
