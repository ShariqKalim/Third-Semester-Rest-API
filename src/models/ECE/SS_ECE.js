const mongoose = require("mongoose");

const SSECESchema = new mongoose.Schema({
  topic: {
    type: String,
    require: true,
    trim: true,
  },
  date: {
    type: String,
    require: true,
    trim: true,
  },
  time: {
    type: String,
    require: true,
    trim: true,
  },
  link: {
    type: String,
    require: true,
    trim: true,
  },
});

const SSECE = new mongoose.model("SS_ECE", SSECESchema);

module.exports = SSECE;
