const mongoose = require("mongoose");

const AEEESchema = new mongoose.Schema({
  topic: {
    type: String,
    require: true,
    trim: true,
  },
  date: {
    type: String,
    require: true,
    trim: true,
  },
  time: {
    type: String,
    require: true,
    trim: true,
  },
  link: {
    type: String,
    require: true,
    trim: true,
  },
});

const AEEE = new mongoose.model("AE_EE", AEEESchema);

module.exports = AEEE;
