const mongoose = require("mongoose");

const MATHEESchema = new mongoose.Schema({
  topic: {
    type: String,
    require: true,
    trim: true,
  },
  date: {
    type: String,
    require: true,
    trim: true,
  },
  time: {
    type: String,
    require: true,
    trim: true,
  },
  link: {
    type: String,
    require: true,
    trim: true,
  },
});

const MATHEE = new mongoose.model("MATH_EE", MATHEESchema);

module.exports = MATHEE;
