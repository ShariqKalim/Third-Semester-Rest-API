const mongoose = require("mongoose");

const NMEESchema = new mongoose.Schema({
  topic: {
    type: String,
    require: true,
    trim: true,
  },
  date: {
    type: String,
    require: true,
    trim: true,
  },
  time: {
    type: String,
    require: true,
    trim: true,
  },
  link: {
    type: String,
    require: true,
    trim: true,
  },
});

const NMEE = new mongoose.model("NM_EE", NMEESchema);

module.exports = NMEE;
