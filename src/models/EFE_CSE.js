const mongoose = require("mongoose");

const EFECSESchema = new mongoose.Schema({
  topic: {
    type: String,
    require: true,
    trim: true,
  },
  date: {
    type: String,
    require: true,
    trim: true,
  },
  time: {
    type: String,
    require: true,
    trim: true,
  },
  link: {
    type: String,
    require: true,
    trim: true,
  },
});

const EFECSE = new mongoose.model("EFE_CSE", EFECSESchema);

module.exports = EFECSE;
