const mongoose = require("mongoose");

const ITWCSESchema = new mongoose.Schema({
  topic: {
    type: String,
    require: true,
    trim: true,
  },
  date: {
    type: String,
    require: true,
    trim: true,
  },
  time: {
    type: String,
    require: true,
    trim: true,
  },
  link: {
    type: String,
    require: true,
    trim: true,
  },
});

const ITWCSE = new mongoose.model("ITW_CSE", ITWCSESchema);

module.exports = ITWCSE;
