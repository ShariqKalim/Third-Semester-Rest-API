const mongoose = require("mongoose");

const ADEITSchema = new mongoose.Schema({
  topic: {
    type: String,
    require: true,
    trim: true,
  },
  date: {
    type: String,
    require: true,
    trim: true,
  },
  time: {
    type: String,
    require: true,
    trim: true,
  },
  link: {
    type: String,
    require: true,
    trim: true,
  },
});

const ADEIT = new mongoose.model("ADE_IT", ADEITSchema);

module.exports = ADEIT;
