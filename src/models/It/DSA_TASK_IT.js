const mongoose = require("mongoose");

const DSATASKITSchema = new mongoose.Schema({
  topic: {
    type: String,
    require: true,
    trim: true,
  },
  date: {
    type: String,
    require: true,
    trim: true,
  },
  time: {
    type: String,
    require: true,
    trim: true,
  },
  link: {
    type: String,
    require: true,
    trim: true,
  },
});

const DSATASKIT = new mongoose.model("DSA_IT_TASK", DSATASKITSchema);

module.exports = DSATASKIT;
