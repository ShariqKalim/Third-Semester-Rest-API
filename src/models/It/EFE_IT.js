const mongoose = require("mongoose");

const EFEITSchema = new mongoose.Schema({
  topic: {
    type: String,
    require: true,
    trim: true,
  },
  date: {
    type: String,
    require: true,
    trim: true,
  },
  time: {
    type: String,
    require: true,
    trim: true,
  },
  link: {
    type: String,
    require: true,
    trim: true,
  },
});

const EFEIT = new mongoose.model("EFE_IT", EFEITSchema);

module.exports = EFEIT;
