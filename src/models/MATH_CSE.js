const mongoose = require("mongoose");

const MATHCSESchema = new mongoose.Schema({
  topic: {
    type: String,
    require: true,
    trim: true,
  },
  date: {
    type: String,
    require: true,
    trim: true,
  },
  time: {
    type: String,
    require: true,
    trim: true,
  },
  link: {
    type: String,
    require: true,
    trim: true,
  },
});

const MATHCSE = new mongoose.model("MATH_CSE", MATHCSESchema);

module.exports = MATHCSE;
